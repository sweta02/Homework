package hw1;

public class DoWhileLoop {

    public static void main(String[] args) {
        //this is to print 10 even numbers and 10 odd numbers

        int a =2;
        do {
            System.out.println(a);
            a+=2;
        }while(a<=20);


        int b =1;
        do{
            System.out.println(b);
            b+=2;
        }while(b<=20);

    }
}