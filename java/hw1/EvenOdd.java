package hw1;

public class EvenOdd {

    public static void main(String[] args) {
        // HW 4 :this is to check if the given number is even or odd using If else
        int k = 369;

        if (k%2==0){
            // if the remainder is zero than the number is even
            System.out.println("This is even number");
        }else
            // if the remainder is not zero than the number is odd
            System.out.println("This is odd number");

    }
}
