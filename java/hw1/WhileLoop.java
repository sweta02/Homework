package hw1;

public class WhileLoop {

    public static void main(String[] args) {
        // this is to write 10 even and odd numbers using while loop
        int a =2;
        while(a<=20){
            System.out.println(a);
            a+=2;
        }

        int b =1;
        while(b<=20){
            System.out.println(b);
            b+=2;
        }
    }
}
